name ="Alice"
age = 27
'''
f/F: allows you to write any valid python expression inside a set of braces {}
'''
print(f"My name is {name} and I am {age} years old.")
print("My name is {name} and I am {age} years old.")
'''
Expressions inside f-strings are as follows:
f-strings automatically handle type conversion, 
so you can directly include integers, floats, 
or any other data type within the string.
'''
print(f"In 5 years, I'll be {age+5} years old.")













