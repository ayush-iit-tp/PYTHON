# STRINGS

# name ="Ayush"
name ='AyushYadav'
name2 ="AyushYadav"
name3 = '''Ayush Yadav'''

# Three Methods to describe a String

print(name)

print(name[0:3])
print(name[1:6])

# There are so many methods in strings.
print("All in Uppercase",name.upper())
print("All in Lowercase",name.lower())
print("First Alphabet is in capital",name.capitalize())
print(name.title())
print(name.count("a"))
print(name.count("A"))

print(name.isalnum()) # Return True
# if the string is an alpha-numeric string, False otherwise

print(name.isalpha())
print(name.isdigit())
print(name.islower())
print(name.isupper())



















































